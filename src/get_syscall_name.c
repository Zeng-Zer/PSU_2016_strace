char	*get_syscall_name(int num)
{
  if (num < 0 || (unsigned long)num >= sizeof(g_syscalls) / sizeof(char*))
    return (NULL);
  return (g_syscalls[num]);
}

int	get_syscall_number_params(int num)
{
  int	i;

  if (num < 0 || (unsigned long)num >= sizeof(g_syscalls) / sizeof(char*))
    return (-1);
  i = 0;
  while (g_syscalls_proto[i].name
	 && strcmp(g_syscalls_proto[i].name, g_syscalls[num]))
    i++;
  if (!g_syscalls_proto[i].name)
    return (-1);
  return (g_syscalls_proto[i].argc);
}
